  getSamlingObject: function (array) {
    let returnObject = {
		object: [], 
		 
	    } 
        // console.log(array)

    array.forEach(element => {
        switch (element.type)
        { 
            case "tegneserie":
                element.indices.forEach(index => {
                    returnObject.object.push (JSON.parse(this.samling.tegneserie[index]))
                });
                break;
            case "utfoldelse":
                element.indices.forEach(index => {
                    returnObject.object.push (JSON.parse(this.samling.utfoldelse[index]))
                });
                break;
            case "husflid":
                element.indices.forEach(index => {
                    returnObject.object.push (JSON.parse(this.samling.husflid[index]))
                });
                break;
            
            default:
                console.log(element.type,"element.type is not a valid input to getSamlingObject");
        }

    });

    console.log("returnObject",returnObject)
    return returnObject

  },
